// Function Definitions

void sort (int arr[], int SIZE)
{
    int minimum = 0;

    for (int i = 0; i < SIZE - 1; ++i)
    {
        minimum = i;
        for (int j = i + 1; j < SIZE; ++j)
        {
            if (arr[j] < arr[minimum])
            {
                minimum = j;
            }
        }
        swap(&arr[i], &arr[minimum]);
    }
}